export const searchPageQuery = /* GraphQL */ `
  query SearchPage(
    $filter: SearchablePostFilterInput
    $sort: [SearchablePostSortInput]
    $limit: Int
    $nextToken: String
    $from: Int
    $aggregates: [SearchablePostAggregationInput]
  ) {
    searchPosts(
      filter: $filter
      sort: $sort
      limit: $limit
      nextToken: $nextToken
      from: $from
      aggregates: $aggregates
    ) {
      items {
        id
        title
        url
        owner
        site
        isUpvoted
        upvoteCount
        commentCount
      }
    }
  }
`;
